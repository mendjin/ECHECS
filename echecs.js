//creer une fonction pour ne pas supprimer le meme element appartenant au meme groupe

function elementDifferent() {
    document.querySelectorAll('.box').forEach(i1 => {
        if (i1.style.backgroundColor == 'yellow') {

            document.querySelectorAll('.box').forEach(i2 => {

                if (i2.style.backgroundColor == 'green' && i2.innerText.length !== 0) {


                    greenText = i2.innerText

                    yellowText = i1.innerText

                    yellowColor = ((Array.from(yellowText)).shift()).toString()
                    greenColor = ((Array.from(greenText)).shift()).toString()

                    getId = i2.id
                    arr = Array.from(getId)
                    arr.shift()
                    aside = eval(arr.pop())
                    aup = eval(arr.shift())
                    a = aside + aup
            
                    if (a % 2 == 0 && yellowColor == greenColor) {
                        i2.style.backgroundColor = 'rgb(240, 201, 150)'
                    }
                    if (a % 2 !== 0 && yellowColor == greenColor) {
                        i2.style.backgroundColor = 'rgb(100, 75, 43)'
                    }
                }
            })
        }
    })
}

