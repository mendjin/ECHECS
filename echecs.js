//gestion des couleurs 

function coloriage() {
    const couleur = document.querySelectorAll('.box')

    couleur.forEach(couleur => {

        getId = couleur.id
        arr = Array.from(getId)
        arr.shift()
        aside = eval(arr.pop())
        aup = eval(arr.shift())
        a = aside + aup

        if (a % 2 == 0) {
            couleur.style.backgroundColor = '#EEEED2'
        }
        if (a % 2 !== 0) {
            couleur.style.backgroundColor = 'rgb(120, 75, 43)'
        }

    })
}
